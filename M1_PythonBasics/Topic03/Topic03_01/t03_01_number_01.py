print("整数与浮点数示例：")
x = 3
print(x)
print(type(x))

y = 4.5
print(y)
print(type(y))

print("整数与浮点数的相互转换：")
m = 5
n = float(m)
print(n)
print(type(n))

s = 3.9
t = int(s)
print(t)
print(type(t))

w = 3+4.5
print(w)
print(type(w))

