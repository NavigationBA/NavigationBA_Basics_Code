print("元组的索引和切片")

fruits = ("apple", "banana", "cherry", "date", "elderberry")
print(fruits[0]) 
print(fruits[-1]) 
print(fruits[1:4]) 
print(fruits[:3]) 
print(fruits[2:]) 

