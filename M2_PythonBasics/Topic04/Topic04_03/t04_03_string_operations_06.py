print("文本处理型的方法：")

print(" Hello ".strip())
print(" Hello ".rstrip())
print(" Hello ".lstrip())

print("-Hello-".strip("-"))
print("-Hello-".rstrip("-"))
print("-Hello-".lstrip("-"))

print("hi".center(6))
print("hi".ljust(6))
print("hi".rjust(6))

print("hi".center(6, "-"))
print("hi".ljust(6, "-"))
print("hi".rjust(6, "-"))

print("54".zfill(4))