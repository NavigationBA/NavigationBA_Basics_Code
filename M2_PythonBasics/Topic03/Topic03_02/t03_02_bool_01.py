print("布尔型基本示例：")

a = True
b = False

print(a)
print(b)

print(type(a))
print(type(b))

print("布尔型转为数字型：")
print(int(True))
print(int(False))
print(float(True))
print(float(False))
print(True + True)
print(True + False)
print(True * 10)

print("数字型转为布尔型：")
print(bool(1))
print(bool(1.0))
print(bool(-2))
print(bool(100))
print(bool(0))
print(bool(0.0))