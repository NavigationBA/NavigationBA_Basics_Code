print("算数运算符练习：")
a = 7
b = 3
print(a + b)   # 10
print(a - b)   # 4
print(a * b)   # 21
print(a / b)   # 2.333...
print(a // b)  # 2
print(a % b)   # 1
print(a ** b)  # 343
print(-a + -b) # -10
print(+a + -b) # 4

print("运算顺序练习：")
print(2 + 3 * 4)     # 14
print((2 + 3) * 4)   # 20
print(2 ** 3 ** 2)   # 512
print((2 ** 3) ** 2) # 64